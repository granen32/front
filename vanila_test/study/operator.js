// 연산자
let a = 1;
let b = 2;
// 이런거를 보통 대입 연산자라 한다.

console.log(a + b);
console.log(a * b);
console.log(a - b);
console.log(a / b);
console.log(a % b);

// 이런 거를 산술 연산자라 한다

let c = "1";
let d ="2";
console.log(c+d);
// 두개 이상의 변수를 연결 시켜주는 거를 연결 연산자라 한다.

a += 10;
// 이런 경우는 복합 연산자라 한다.
// 사칙연산에서 다루는 걸 모두 다룰 수 있다.
console.log(a);

// 증감 연산식 -> 숫자형에만 사용할 수 있다.

a++;
console.log(a);
